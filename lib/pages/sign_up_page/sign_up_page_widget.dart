                              await showDialog(
                                context: context,
                                builder: (alertDialogContext) {
                                  return AlertDialog(
                                    title: Text('注意'),
                                    content: Text(
                                        '利用規約及びプライバシーポリシーに同意しない場合、本アプリをご利用になれません。'),
                                    actions: [
                                      TextButton(
                                        onPressed: () =>
                                            Navigator.pop(alertDialogContext),
                                        child: Text('閉じる'),
                                      ),
                                    ],
                                  );
                                },
                              );
                              return;
                            }

                            _navigate();
                          },
                        ),
                      ),
                    ]
                        .divide(SizedBox(height: 15.0))
                        .addToStart(SizedBox(height: 20.0))
                        .addToEnd(SizedBox(height: 20.0)),
                  ),
                ),
              ]
                  .addToStart(SizedBox(height: 20.0))
                  .addToEnd(SizedBox(height: 20.0)),
            ),
          ),
        ),
      ),
    );
  }
